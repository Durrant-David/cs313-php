CREATE TABLE catalog_category (
  id serial PRIMARY KEY,
  name varchar(255) NOT NULL,
  parent_category_id INT REFERENCES catalog_category(id)
);

CREATE TABLE catalog_status (
  id serial PRIMARY KEY,
  name varchar(255) NOT NULL UNIQUE
);

CREATE TABLE catalog (
  id serial PRIMARY KEY,
  name varchar(255) NOT NULL,
  catalog_category_id INT REFERENCES catalog_category(id),
  location varchar(255),
  catalog_status_id INT REFERENCES catalog_status(id),
  image varchar(255),
  min_quantity int,
  description text
);

CREATE TABLE dimmer (
  id serial PRIMARY KEY,
  name varchar(255) NOT NULL UNIQUE
);

CREATE TABLE dimmer_circuit (
  id serial PRIMARY KEY,
  dimmer_id INT REFERENCES dimmer(id),
  address int NOT NULL
);

CREATE TABLE fixture_status (
  id serial PRIMARY KEY,
  name varchar(255) NOT NULL UNIQUE
);

CREATE TABLE lookup_type (
  id serial PRIMARY KEY,
  name varchar(255) NOT NULL UNIQUE
);

CREATE TABLE lookup_level (
  id serial PRIMARY KEY,
  name varchar(255) NOT NULL UNIQUE
);

CREATE TABLE lookup_catwalk (
  id serial PRIMARY KEY,
  name varchar(255) NOT NULL UNIQUE
);

CREATE TABLE lookup_chair (
  id serial PRIMARY KEY,
  name varchar(255) NOT NULL UNIQUE
);

CREATE TABLE lookup_position (
  id serial PRIMARY KEY,
  name varchar(255) NOT NULL UNIQUE
);

CREATE TABLE fixture (
  id serial PRIMARY KEY,
  lookup_catwalk_id INT REFERENCES lookup_catwalk(id),
  number int NOT NULL,
  catalog_id INT REFERENCES catalog(id),
  dimmer_circuit_id INT REFERENCES dimmer_circuit(id),
  fixture_status_id INT REFERENCES fixture_status(id),
  description text
);

CREATE TABLE lookup (
  id serial PRIMARY KEY,
  lookup_type_id INT REFERENCES lookup_type(id),
  lookup_level_id INT REFERENCES lookup_level(id),
  lookup_catwalk_id INT REFERENCES lookup_catwalk(id),
  lookup_chair_id INT REFERENCES lookup_chair(id),
  lookup_position_id INT REFERENCES lookup_position(id),
  fixture_id INT REFERENCES fixture(id)
);

CREATE TABLE user_access_level (
  id serial PRIMARY KEY,
  name varchar(255) NOT NULL UNIQUE
);

CREATE TABLE "user" (
  id serial PRIMARY KEY,
  username varchar(255) NOT NULL UNIQUE,
  first_name varchar(255) NOT NULL,
  last_name varchar(255) NOT NULL,
  user_access_level_id INT REFERENCES user_access_level(id),
  expiration date
);

